type User = models.User

// GetUsers returns all users from database
func GetUsers() ([]User, error) {
	// 1. initializes an empty user struct
	var users []User

	// 2. get rows from database
	query := `
		SELECT
			id,
			name,
			login,
			password
		FROM users
		ORDER BY id DESC
		`
	rows, err := database.DB.Query(query)
	if err != nil {
		log.Logger.Error().Int("error_code", http.StatusInternalServerError).Msg("Error obtaining users.")
		return users, err
	}
	defer rows.Close()

	// 3. loads the user struct
	for rows.Next() {
		var u User
		if err := rows.Scan(&u.ID, &u.Name, &u.Login, &u.Password); err != nil {
			log.Logger.Error().Int("error_code", http.StatusInternalServerError).Msg("Error parsing users.")
			return users, err
		}
		users = append(users, u)
	}

	return users, nil
}

// GetUser returns a user by id
func GetUser(id int64) (User, error) {
	// 1. initializes empty struct
	var user User

	// 2. get row from database
	query := `
		SELECT
			id,
			name,
			login,
			password
		FROM users
		WHERE id = ?
		ORDER BY id DESC
		`
	err := database.DB.QueryRow(query, id).
		Scan(&user.ID, &user.Name, &user.Login, &user.Password)
	if err != nil {
		if err == sql.ErrNoRows {
			log.Logger.Error().
				Int("error_code", http.StatusNotFound).
				Err(err).
				Msg("User not found.")
			return user, errors.New("User not found.")
		} else {
			log.Logger.Error().
				Int("error_code", http.StatusInternalServerError).
				Err(err).
				Msg("Error obtaining user.")
			return user, errors.New("Error obtaining user.")
		}
	}

	return user, nil
}


package repository

import (
	"database/sql"
	"errors"
	"net/http"
	"strings"

	"github.com/eusebioleite/selfin/database"
	"github.com/eusebioleite/selfin/models"
	"github.com/eusebioleite/selfin/utils"
	"github.com/rs/zerolog/log"
)

type Category = models.Category

// GetCategories returns all categories from database
func GetCategories() ([]Category, error) {
	// 1. initializes an empty category struct
	var categories []Category

	// 2. get rows from database
	query := `
	SELECT
		id,
		description
	FROM categories
	ORDER BY id DESC
	`
	rows, err := database.DB.Query(query)
	if err != nil {
		log.Logger.Error().Int("error_code", http.StatusInternalServerError).Msg("Error obtaining categories.")
		return categories, err
	}
	defer rows.Close()

	// 3. loads the category struct
	for rows.Next() {
		var c Category
		if err := rows.Scan(&c.ID, &c.Description); err != nil {
			log.Logger.Error().Int("error_code", http.StatusInternalServerError).Msg("Error parsing categories.")
			return categories, err
		}
		categories = append(categories, c)
	}

	return categories, nil
}

// GetCategory returns a category by id
func GetCategory(id int64) (Category, error) {
	// 1. initializes empty struct
	var category Category

	// 2. get row from database
	query := `
	SELECT
		id,
		description
	FROM categories
	WHERE id = ?
	ORDER BY id DESC
	`
	err := database.DB.QueryRow(query, id).
		Scan(&category.ID, &category.Description)
	if err != nil {
		if err == sql.ErrNoRows {
			log.Logger.Error().
				Int("error_code", http.StatusNotFound).
				Err(err).
				Msg("category not found.")
			return category, errors.New("category not found.")
		} else {
			log.Logger.Error().
				Int("error_code", http.StatusInternalServerError).
				Err(err).
				Msg("Error obtaining category.")
			return category, errors.New("Error obtaining category.")
		}
	}

	return category, nil
}

// NewCategory creates a new category.
// this function receives a category struct and returns the struct with the id returned by the database
func NewCategory(category *Category) error {

	// 1. executes the insert query with values from the struct provided
	query := `
	INSERT INTO categories (description)
	VALUES (?, ?, ?)
	`
	res, err := database.DB.Exec(query, category.Description)
	if err != nil {
		log.Logger.Error().
			Int("error_code", http.StatusInternalServerError).
			Err(err).
			Msg("Error creating new category.")
		return err
	}

	// 2. gets the id generated by the database
	id, err := res.LastInsertId()
	if err != nil {
		log.Logger.Error().
			Int("error_code", http.StatusInternalServerError).
			Err(err).
			Msg("Error obtaining 'id' generated for category.")
		return err
	}

	// 3. updates the struct with the id
	category.ID = id

	return nil
}

// UpdateCategory updates a category.
func UpdateCategory(category *Category) error {

	// 1. checks if an id was sent
	if category.ID == 0 {
		log.Logger.Error().
			Int("error_code", http.StatusBadRequest).
			Int64("category_id", category.ID).
			Msg("No ID was provided.")
		return errors.New("no ID was provided")
	}

	// 2. builds the update query
	query := "UPDATE categories SET "
	var args []any

	// 2.1. checks if a description was sent
	if category.Description != "" {
		query += "description = ?, "
		args = append(args, category.Description)
	}

	// 3. checks if any field was sent at all
	if len(args) == 0 {
		log.Logger.Warn().Int64("category_id", category.ID).Msg("No fields to update.")
		return nil
	}

	// 4. removes the last comma
	query = strings.TrimSuffix(query, ", ")

	// 5. adds the where clause
	query += " WHERE id = ?"
	args = append(args, category.ID)

	// 6. executes the query
	res, err := database.DB.Exec(query, args)
	if err != nil {
		log.Logger.Error().
			Int("error_code", http.StatusInternalServerError).
			Int64("category_id", category.ID).
			Err(err).
			Msg("Error updating category.")
		return err
	}
	// 7. checks if any row was updated at all
	if !utils.HasRowsAffected(res) {
		log.Logger.Warn().Int64("category_id", category.ID).Msg("No rows affected.")
	}

	return nil
}

func DeleteCategory(id int64) error {
	// 1. checks if an id was sent at all
	if id == 0 {
		log.Logger.Error().
			Int("error_code", http.StatusBadRequest).
			Int64("category_id", id).
			Msg("No ID was provided.")
		return errors.New("no ID was provided")
	}

	// 2. executes the delete query
	res, err := database.DB.Exec(
		"DELETE FROM categories where id = ?", id)
	if err != nil {
		if utils.IsConstraintViolation(err) {
			log.Logger.Error().
				Int("error_code", http.StatusConflict).
				Int64("category_id", id).
				Err(err).
				Msg("Cannot delete category: it is referenced by one or more transactions.")
			return errors.New("category is in use by one or more transactions")
		}
		log.Logger.Error().
			Int("error_code", http.StatusInternalServerError).
			Int64("category_id", id).
			Err(err).
			Msg("Error deleting category.")
		return err
	}

	// 3. checks if any row was deleted at all
	if !utils.HasRowsAffected(res) {
		log.Logger.Warn().Int64("category_id", id).Msg("No rows affected.")
	}

	return nil
}
